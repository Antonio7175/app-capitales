package com.miempresa.dam2_00.capital;

public class Capital {
	
	private String nombre;
    private String imagen;

    public Capital(String nombre, String imagen) {
        this.nombre = nombre;
        this.imagen = imagen;
    }

    public String getNombre() {
        return nombre;
    }

    public String getImagen() {
        return imagen;
    }
}


